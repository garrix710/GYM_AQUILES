    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:

        String nombre = txtEditNombreAdministrador.getText().trim();
        String correo = txtEditCorreo.getText().trim();
        String telefono = txtEditTelefono.getText().trim();
        char[] password= psw.getPassword();
        String passString = new String(password);

        boolean validar = validarCampos(nombre, correo, telefono);
        if (!validar) {
            return;
        }

        int filaSeleccionada = tableAdministradores.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente para editar.", "Sin selección", JOptionPane.WARNING_MESSAGE);
            return;
        }
        byte[] datosHuella = null;

        try {
            // Intentar obtener una nueva huella
            datosHuella = lector_huella_new.obtenerDatosHuella();
        } catch (IllegalStateException ex) {
            // Si no se capturó ninguna huella, solo mostrar aviso informativo (no es un error)
            System.out.println("No se actualizó la huella: " + ex.getMessage());
            // También puedes mostrar un mensaje opcional al usuario:
            // JOptionPane.showMessageDialog(null, "No se actualizó la huella: " + ex.getMessage(), "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }

        try {

            // Crear nuevo entrenador
            Administrador AdministradorExiste = administradores.get(filaSeleccionada);

            AdministradorExiste.setNombreCompleto(nombre);
            AdministradorExiste.setCorreoElectronico(correo);
            AdministradorExiste.setTelefonoContacto(telefono);
            AdministradorExiste.setPassword(passString);
            // Actualizar huella solo si hay una nueva válida
            if (datosHuella != null && datosHuella.length > 0) {
                AdministradorExiste.setHuellaDactilar(datosHuella);
            }

            // Actualizar en base de datos
            cAdministradores.edit(AdministradorExiste);
            mta.fireTableRowsUpdated(filaSeleccionada, filaSeleccionada);
            lector_huella_new.limpiarDatos();

            JOptionPane.showMessageDialog(this, "Entrenador registrado correctamente.");

        } catch (IllegalStateException ex) {
            JOptionPane.showMessageDialog(this, "Error al obtener huella: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton11ActionPerformed

