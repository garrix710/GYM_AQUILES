    private void btnEditEntrenadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditEntrenadorActionPerformed
        // TODO add your handling code here:
        String nombre = txtEditEntrenador.getText().trim();
        String correo = txtEditCorreo.getText().trim();
        String telefono = txtEditTelefono.getText().trim();

        boolean validar = validarCampos(nombre, correo, telefono);
        if (!validar) {
            return;
        }

        int filaSeleccionada = tableEntrenadores.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente para editar.", "Sin selección", JOptionPane.WARNING_MESSAGE);
            return;
        }
        byte[] datosHuella = null;

        try {
            // Intentar obtener una nueva huella
            datosHuella = lector_huella_new.obtenerDatosHuella();
        } catch (IllegalStateException ex) {
            // Si no se capturó ninguna huella, solo mostrar aviso informativo (no es un error)
            System.out.println("No se actualizó la huella: " + ex.getMessage());
            // También puedes mostrar un mensaje opcional al usuario:
            // JOptionPane.showMessageDialog(null, "No se actualizó la huella: " + ex.getMessage(), "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }

        try {

            // Crear nuevo entrenador
            Entrenador EntrenadorExiste = entrenadores.get(filaSeleccionada);

            EntrenadorExiste.setNombreCompleto(nombre);
            EntrenadorExiste.setCorreoElectronico(correo);
            EntrenadorExiste.setTelefonoContacto(telefono);
            // Actualizar huella solo si hay una nueva válida
            if (datosHuella != null && datosHuella.length > 0) {
                EntrenadorExiste.setHuellaDactilar(datosHuella);
            }

            // Obtener el nombre de la especialidad seleccionada (String)
            String nombreEspecialidad = (String) comboEditEspecialidad.getSelectedItem();

            // Buscar el objeto Especialidades correspondiente
            Especialidades especialidad = null;
            for (Especialidades esp : especialidades) {
                if (esp.getNombreEspecialidad().equals(nombreEspecialidad)) {
                    especialidad = esp;
                    break;
                }
            }

            if (especialidad != null) {
                List<Especialidades> listaEsp = new ArrayList<>();
                listaEsp.add(especialidad);
                EntrenadorExiste.setEspecialidadesList(listaEsp);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró la especialidad seleccionada.");
            }

            // Actualizar en base de datos
            cEntrenadores.edit(EntrenadorExiste);
            mte.fireTableRowsUpdated(filaSeleccionada, filaSeleccionada);
            lector_huella_new.limpiarDatos();

            JOptionPane.showMessageDialog(this, "Entrenador registrado correctamente.");

        } catch (IllegalStateException ex) {
            JOptionPane.showMessageDialog(this, "Error al obtener huella: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnEditEntrenadorActionPerformed

