    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed

        // Obtener el índice de la fila seleccionada en la tabla
        int filaSeleccionada = tableCliente.getSelectedRow();

        // Verificar si hay alguna fila seleccionada
        if (filaSeleccionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Por favor, seleccione un cliente para eliminar.",
                "Ninguna selección",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Confirmar eliminación con el usuario
        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this,
            "¿Está seguro de que desea eliminar éste cliente?",
            "Confirmar eliminación",
            javax.swing.JOptionPane.YES_NO_OPTION);

        // Si el usuario confirma la eliminación
        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            try {
                // Obtener el cliente seleccionado
                Clientes clienteSeleccionado = clientes.get(filaSeleccionada);

                // Obtener el ID del cliente (idcliente es un Integer según la clase TutorJpaController)
                Integer idCliente = clienteSeleccionado.getClienteId();

                // Eliminar de la base de datos usando el ID
                cClientes.destroy(idCliente);

                // Eliminar de la lista local
                clientes.remove(filaSeleccionada);

                // Actualizar la tabla
                mtc.fireTableDataChanged();

                // Mostrar mensaje de éxito
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Cliente eliminado correctamente.",
                    "Éxito",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);

            } catch (NonexistentEntityException e) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Error: El Cliente ya no existe en la base de datos.",
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
                // Actualizar la tabla de todas formas
                clientes.remove(filaSeleccionada);
                mtc.fireTableDataChanged();
            } catch (HeadlessException e) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Error al eliminar el cliente: " + e.getMessage(),
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            } catch (IllegalOrphanException ex) {
                Logger.getLogger(PanelClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminarClienteActionPerformed

    private void btnMembresiasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMembresiasActionPerformed
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");

        int filaSeleccionada = tableCliente.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un cliente.");
            return;
        }

        // Obtener cliente desde el modelo
        Clientes clienteSeleccionado = mtc.getClienteAt(filaSeleccionada);

        // Buscar la membresía correspondiente al cliente
        Membresia membresiaSeleccionada = null;
        for (Membresia m : membresias) {
            if (m.getClienteId().getClienteId().equals(clienteSeleccionado.getClienteId())) {
                membresiaSeleccionada = m;
                break;
            }
        }

        if (membresiaSeleccionada == null) {
            JOptionPane.showMessageDialog(this, "No se encontró una membresía para este cliente.");
            return;
        }

        // Buscar el pago correspondiente a la membresía
        Pagomembresia pagoSeleccionado = null;
        for (Pagomembresia p : pagomembresias) {
            if (p.getMembresiaId().getMembresiaId().equals(membresiaSeleccionada.getMembresiaId())) {
                pagoSeleccionado = p;
                break;
            }
        }

        if (pagoSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "No se encontró un pago para esta membresía.");
            return;
        }

        // Asignar los valores a los labels y combos
        labelNombreCliente.setText(clienteSeleccionado.getNombreCompleto());
        labelID.setText(Integer.toString(clienteSeleccionado.getClienteId()));
        labelEstado.setText(membresiaSeleccionada.getEstadoMembresia());
        comboTipoMembresia.setSelectedItem(membresiaSeleccionada.getTipoMembresia());
        labelFechaInicio.setText(formatoFecha.format(membresiaSeleccionada.getFechaInicio()));
        labelFechaFin.setText(formatoFecha.format(membresiaSeleccionada.getFechaVencimiento()));
        labelPrecio.setText(String.valueOf(membresiaSeleccionada.getPrecio()));
        comboMetodoPago.setSelectedItem(pagoSeleccionado.getMetodoPago());

        jTabbedPane1.setEnabledAt(3, true);
        jTabbedPane1.setSelectedIndex(3);

    }//GEN-LAST:event_btnMembresiasActionPerformed

