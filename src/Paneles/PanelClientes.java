    private void btnEditCLIENTEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditCLIENTEActionPerformed
        // TODO add your handling code here:
        String campo2 = txtEditNombre.getText().trim();
        String email = txtEditEmail.getText().trim();
        String telefono = txtEditTelefono.getText().trim();

        boolean validar = validarCampos(campo2, email, telefono);
        if (!validar) {
            return;
        }

        int filaSeleccionada = tableCliente.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente para editar.", "Sin selección", JOptionPane.WARNING_MESSAGE);
            return;
        }

        byte[] datos_huella = null;

        try {
            // Intentar obtener una nueva huella
            datos_huella = lector_huella_new.obtenerDatosHuella();
        } catch (IllegalStateException ex) {
            // Si no se capturó ninguna huella, solo mostrar aviso informativo (no es un error)
            System.out.println("No se actualizó la huella: " + ex.getMessage());
            // También puedes mostrar un mensaje opcional al usuario:
            // JOptionPane.showMessageDialog(null, "No se actualizó la huella: " + ex.getMessage(), "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }

        try {
            Clientes clienteExistente = clientes.get(filaSeleccionada);

            // Actualizar datos básicos
            clienteExistente.setNombreCompleto(campo2);
            clienteExistente.setCorreoElectronico(email);
            clienteExistente.setTelefonoContacto(telefono);

            // Actualizar huella solo si hay una nueva válida
            if (datos_huella != null && datos_huella.length > 0) {
                clienteExistente.setHuellaDactilar(datos_huella);
            }

            // Actualizar en base de datos
            cClientes.edit(clienteExistente);
            mtc.fireTableRowsUpdated(filaSeleccionada, filaSeleccionada);
            lector_huella_new.limpiarDatos();

            JOptionPane.showMessageDialog(null, "Cliente actualizado correctamente.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al actualizar cliente: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnEditCLIENTEActionPerformed

