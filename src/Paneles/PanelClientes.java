    private void buscarYSeleccionarNombreEnTabla(String nombreBuscado) {
    MtCliente modelo = (MtCliente) tableCliente.getModel();

    for (int fila = 0; fila < modelo.getRowCount(); fila++) {
        Clientes cliente = modelo.getClienteAt(fila);
        if (cliente != null && cliente.getNombreCompleto().equalsIgnoreCase(nombreBuscado)) {
            tableCliente.setRowSelectionInterval(fila, fila);
            tableCliente.scrollRectToVisible(new Rectangle(tableCliente.getCellRect(fila, 0, true)));
            JOptionPane.showMessageDialog(this, "Cliente encontrado.");
            txtNombreCliente.setText("");  // <-- Limpiar el campo de texto
            return;
        }
    }

    JOptionPane.showMessageDialog(this, "Cliente no encontrado.");
}
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        String campo2 = jTextField2.getText().trim();
        String email = jTextField3.getText().trim();
        String telefono = numero.getText().trim();

        boolean validar = validarCampos(campo2, email, telefono);
        if (!validar) {
            return;
        }

        Date fechaActual = new Date();
        try {
            byte[] datos_huella = lector_huella.obtenerDatosHuella(); 
            if (datos_huella == null || datos_huella.length == 0) {
                JOptionPane.showMessageDialog(null, "No se pudo obtener la huella digital.", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Detener ejecución si datos_huella está vacío
            }

            // 2. Verificar si el tutor ya existe (por número de tarjeta, por ejemplo)
            for (Clientes c : clientes) {
                if (c.getNombreCompleto().equalsIgnoreCase(campo2)) {
                    javax.swing.JOptionPane.showMessageDialog(this, "El cliente con el nombre " + campo2 + " ya existe.", "Dato Duplicado", javax.swing.JOptionPane.ERROR_MESSAGE);
                    return; // Detener la ejecución si el tutor ya existe
                }
                if (c.getCorreoElectronico().equals(email)) {
                    JOptionPane.showMessageDialog(this, "El cliente con el correo \"" + email + "\" ya existe.", "Dato duplicado", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (c.getTelefonoContacto().equals(telefono)) {
                    JOptionPane.showMessageDialog(this, "El cliente con el Numero de telefono \"" + telefono + "\" ya existe.", "Dato duplicado", JOptionPane.ERROR_MESSAGE);
                    return;
                }

            }

            cliente=new Clientes();
            cliente.setNombreCompleto(campo2);
            cliente.setCorreoElectronico(email);
            cliente.setTelefonoContacto(telefono);
            cliente.setFechaRegistro(fechaActual);
            cliente.setHuellaDactilar(datos_huella);

            cClientes.create(cliente);
            clientes.add(cliente);
            mtc.fireTableDataChanged();

            JOptionPane.showMessageDialog(null, "Cliente registrado correctamente.");
            // Limpiar campos después del registro exitoso
            jTextField2.setText("");
            jTextField3.setText("");
            numero.setText("");
            lector_huella.limpiarDatos();

        } catch (IllegalStateException ex) {
            JOptionPane.showMessageDialog(null, "Error al obtener huella: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    public boolean validarCampos(String campo2, String email, String telefono) {
    // Validación de campo2
    if (campo2.isEmpty()) {
        JOptionPane.showMessageDialog(null, "El campo de nombre no debe estar vacío.");
        return false;
    } else if (!campo2.matches("[a-zA-ZáéíóúÁÉÍÓÚñÑ ]+")) {
        JOptionPane.showMessageDialog(null, "El nombre solo debe contener letras y espacios.");
        return false;
    }

    // Validación de email
    String emailRegex = "^[a-zA-Z0-9.]+@[a-zA-Z0-9-]+\\.[a-zA-Z]{2,6}$";
    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(null, "El correo electrónico no debe estar vacío.");
        return false;
    } else if (!email.matches(emailRegex)) {
        JOptionPane.showMessageDialog(null, "El correo electrónico no cumple con el formato estándar (ej: usuario@dominio.com).");
        return false;
    }

    // Validación de teléfono
    if (telefono.isEmpty()) {
        JOptionPane.showMessageDialog(null, "El número de teléfono no debe estar vacío.");
        return false;
    } else if (!telefono.matches("\\d+")) {
        JOptionPane.showMessageDialog(null, "El teléfono solo debe contener números.");
        return false;
    } else if (telefono.length() != 10) {
        JOptionPane.showMessageDialog(null, "El número de teléfono debe contener exactamente 10 dígitos.");
        return false;
    }

    return true;
} 
