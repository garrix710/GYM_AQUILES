    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombreEntrenador.getText().trim();
        String correo = txtCorreoEntrenador.getText().trim();
        String telefono = txtTelefonoEntrenador.getText().trim();

        boolean validar = validarCampos(nombre, correo, telefono);
        if (!validar) {
            return;
        }

        try {
            byte[] datosHuella = lector_huella.obtenerDatosHuella();
            if (datosHuella == null || datosHuella.length == 0) {
                JOptionPane.showMessageDialog(this, "No se pudo obtener la huella digital.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Verificar si el entrenador ya existe por nombre
            for (Entrenador e : entrenadores) {
                if (e.getNombreCompleto().equalsIgnoreCase(nombre)) {
                    JOptionPane.showMessageDialog(this, "El entrenador con el nombre \"" + nombre + "\" ya existe.", "Dato duplicado", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (e.getCorreoElectronico().equals(correo)) {
                    JOptionPane.showMessageDialog(this, "El entrenador con el correo \"" + nombre + "\" ya existe.", "Dato duplicado", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (e.getTelefonoContacto().equals(telefono)) {
                    JOptionPane.showMessageDialog(this, "El entrenador con el Numero de telefono \"" + nombre + "\" ya existe.", "Dato duplicado", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (Arrays.equals(e.getHuellaDactilar(), datosHuella)) {
                JOptionPane.showMessageDialog(this, "Ya existe un entrenador registrado con esta misma huella digital.", "Huella duplicada", JOptionPane.ERROR_MESSAGE);
                return;
        }
            }

            // Crear nuevo entrenador
            Entrenador entrenador = new Entrenador();
            entrenador.setNombreCompleto(nombre);
            entrenador.setCorreoElectronico(correo);
            entrenador.setTelefonoContacto(telefono);
            entrenador.setHuellaDactilar(datosHuella); // Asegúrate que el atributo sea 'huella' o 'huellaDactilar' según tu entidad

            // Obtener el nombre de la especialidad seleccionada (String)
            String nombreEspecialidad = (String) comboEspecialidades.getSelectedItem();

            // Buscar el objeto Especialidades correspondiente
            Especialidades especialidad = null;
            for (Especialidades esp : especialidades) {
                if (esp.getNombreEspecialidad().equals(nombreEspecialidad)) {
                    especialidad = esp;
                    break;
                }
            }

            if (especialidad != null) {
                List<Especialidades> listaEsp = new ArrayList<>();
                listaEsp.add(especialidad);
                entrenador.setEspecialidadesList(listaEsp);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró la especialidad seleccionada.");
            }

            // Guardar en la base de datos
            cEntrenadores.create(entrenador); // Asegúrate de tener bien tu controlador JPA
            entrenadores.add(entrenador);
            mte.fireTableDataChanged();

            JOptionPane.showMessageDialog(this, "Entrenador registrado correctamente.");

            // Limpiar campos después del registro exitoso
            txtNombreEntrenador.setText("");
            txtCorreoEntrenador.setText("");
            txtTelefonoEntrenador.setText("");
            comboEspecialidades.setSelectedIndex(0);
            lector_huella.limpiarDatos();

        } catch (IllegalStateException ex) {
            JOptionPane.showMessageDialog(this, "Error al obtener huella: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    public boolean validarCampos(String campo2, String email, String telefono) {
    // Validación de campo2
    if (campo2.isEmpty()) {
        JOptionPane.showMessageDialog(null, "El campo de nombre no debe estar vacío.");
        return false;
    } else if (!campo2.matches("[a-zA-ZáéíóúÁÉÍÓÚñÑ ]+")) {
        JOptionPane.showMessageDialog(null, "El nombre solo debe contener letras y espacios.");
        return false;
    }

    // Validación de email
    String emailRegex = "^[a-zA-Z0-9.]+@[a-zA-Z0-9-]+\\.[a-zA-Z]{2,6}$";
    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(null, "El correo electrónico no debe estar vacío.");
        return false;
    } else if (!email.matches(emailRegex)) {
        JOptionPane.showMessageDialog(null, "El correo electrónico no cumple con el formato estándar (ej: usuario@dominio.com).");
        return false;
    }

    // Validación de teléfono
    if (telefono.isEmpty()) {
        JOptionPane.showMessageDialog(null, "El número de teléfono no debe estar vacío.");
        return false;
    } else if (!telefono.matches("\\d+")) {
        JOptionPane.showMessageDialog(null, "El teléfono solo debe contener números.");
        return false;
    } else if (telefono.length() != 10) {
        JOptionPane.showMessageDialog(null, "El número de teléfono debe contener exactamente 10 dígitos.");
        return false;
    }

    return true;
} 
