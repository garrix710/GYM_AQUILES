    private void btnGuardarMembresiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarMembresiaActionPerformed

    try {
        if (clienteSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Primero selecciona un cliente.");
            return;
        }

        String tipo = comboMembresia.getSelectedItem().toString();
        String estado = "Activo".toLowerCase().trim();

        String precioTexto = txtPrecio.getText().trim();
        if (precioTexto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa el precio de la membresía.");
            return;
        }

        BigDecimal precioDecimal;
        try {
            precioDecimal = new BigDecimal(precioTexto);
            if (precioDecimal.compareTo(BigDecimal.ZERO) <= 0) {
                JOptionPane.showMessageDialog(this, "El precio debe ser mayor a cero.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio ingresado no es válido.");
            return;
        }

        Date fechaInicioDate = chooserInicio.getDate();
        Date fechaVencimientoDate = chooserFin.getDate();

        if (fechaInicioDate == null || fechaVencimientoDate == null) {
            JOptionPane.showMessageDialog(this, "Selecciona ambas fechas.");
            return;
        }

        if (fechaInicioDate.after(fechaVencimientoDate)) {
            JOptionPane.showMessageDialog(this, "La fecha de inicio no puede ser posterior a la fecha de vencimiento.");
            return;
        }

        String metodo = comboMetodoPago1.getSelectedItem().toString();
        if (metodo == null || metodo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecciona un método de pago válido.");
            return;
        }

        Membresia membresia = new Membresia();
        membresia.setClienteId(clienteSeleccionado);
        membresia.setTipoMembresia(tipo);
        membresia.setFechaInicio(fechaInicioDate);
        membresia.setFechaVencimiento(fechaVencimientoDate);
        membresia.setEstadoMembresia(estado);
        membresia.setPrecio(precioDecimal);

        cMembresias.create(membresia);
        membresias.add(membresia);
        mtm.fireTableDataChanged();

        Pagomembresia pago = new Pagomembresia();
        pago.setClienteId(clienteSeleccionado);
        pago.setMembresiaId(membresia);
        pago.setFechaPago(fechaInicioDate);
        pago.setMontoPago(precioDecimal);
        pago.setMetodoPago(metodo);

        cPagomembresias.create(pago);
        pagomembresias.add(pago);
        mtp.fireTableDataChanged();

        JOptionPane.showMessageDialog(this, "Membresía y pago registrados correctamente.");

        // Limpieza
        comboMembresia.setSelectedIndex(0);
        txtPrecio.setText("");
        chooserInicio.setDate(null);
        chooserFin.setDate(null);
        comboMetodoPago1.setSelectedIndex(0);

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar la membresía: " + ex.getMessage());
        ex.printStackTrace();
    }

    }//GEN-LAST:event_btnGuardarMembresiaActionPerformed


